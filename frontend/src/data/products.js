export function getProductById(id) {
    return products.find((p) => p.id === parseInt(id));
}

export function getProductsByCategory(slug) {
    return products.filter((p) => p.categories.includes(slug));
}

export function getFeaturedProducts() {
    return products.filter((p) => p.rating >= 4.5).slice(0, 6);
}

export function getRecommendedProducts(conditionSlug, excludeId = null) {
    return products
        .filter((p) => p.id !== excludeId)
        .filter((p) => p.suitability[conditionSlug] >= 80)
        .sort((a, b) => b.suitability[conditionSlug] - a.suitability[conditionSlug])
        .slice(0, 4);
}

export function searchProducts(query) {
    const q = query.toLowerCase();
    return products.filter(
        (p) =>
            p.name.toLowerCase().includes(q) ||
            p.description.toLowerCase().includes(q) ||
            p.ingredients.some((i) => i.toLowerCase().includes(q))
    );
}
